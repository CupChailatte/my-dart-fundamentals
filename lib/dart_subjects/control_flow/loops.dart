
/*
? What are loops?
* Are used for repeating code multiple times 
*/

void main(){

  //*FOR LOOP 
  for (int i = 0; i <= 5; i++){
    print(i); 
  }

  //*WHILE LOOP
  int count = 0;
  while (count < 10);  
  count++; 
  print("Count : $count");
  

  //*DO WHILE LOOP
  int num = 0; 
  do{
    print(num); 
    num++; 

  } while(num<3); 
}